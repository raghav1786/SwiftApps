import UIKit

extension UIColor {
    static public func backgroundColor() -> UIColor {
        return UIColor(red: 236/255, green: 181/255, blue: 89/255, alpha: 1)
    }
    static public func darkGrayColor() -> UIColor {
        return UIColor(red: 85/255, green: 85/255, blue: 85/255, alpha: 1)
    }
    
}
